    private Paciente buscarPaciente(String cedula) {
        dlm.removeAllElements();

        for (Paciente paciente : listaPacientes.getPacientes()) {
            if (paciente.getCedula().equals(cedula)) {
                dlm.addElement(paciente.toString());
                return paciente;
            }
        }
        return null;

    }
